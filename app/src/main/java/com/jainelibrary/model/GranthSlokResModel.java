package com.jainelibrary.model;

public class GranthSlokResModel {
    String id;
    String shlok_name;
    String keyword;
    String book_count;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getShlok_name() {
        return shlok_name;
    }

    public void setShlok_name(String shlok_name) {
        this.shlok_name = shlok_name;
    }

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    public String getBook_count() {
        return book_count;
    }

    public void setBook_count(String book_count) {
        this.book_count = book_count;
    }
}
